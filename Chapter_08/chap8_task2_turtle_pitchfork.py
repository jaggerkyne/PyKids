    arrow_4 = turtle.Pen()

    arrow_1.forward(100)
    arrow_1.left(90)
    arrow_1.forward(50)
    arrow_1.right(90)
    arrow_1.forward(50)

    arrow_4.forward(100)
    arrow_4.right(90)
    arrow_4.forward(50)
