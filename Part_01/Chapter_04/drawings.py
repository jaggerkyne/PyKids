import turtle

t = turtle.Pen() # create a blank canvas

t.forward(50) # move forward 50 pixel

t.left(90) # turn the turtle 90 degree

t.forward(50)

t.left(90)

t.forward(50)

t.left(90)

t.forward(50)

t.right(90)

t.forward(50)

t.right(90)

t.forward(50)

t.reset() # this clears the canvas and put the turtle back at its starting position

t.forward(50)

t.clear() # this clears the screen and leaves the turtle where it is.

t.backward(100)
t.up() # stop drawing
t.right(90)
t.forward(20)
t.left(90)
t.down()
t.forward(100)

